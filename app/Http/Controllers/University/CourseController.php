     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $input = $request->all();
        $input['status']=1;
        $course = Course::create($input);
        return $this->sendResponse($course,'College created successfully.');
    }

